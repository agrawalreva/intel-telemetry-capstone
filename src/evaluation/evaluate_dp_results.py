"""
evaluate_dp_results.py

Analyzes the metric summary CSVs generated by the DP mechanism files and
produces per-query evaluation statistics for both Gaussian and Laplace.

Changes from original:
  - Added --variant argument (baseline | advance) to select subfolder
  - Updated metric_type mapping from letter codes (A/B/C/D/E) to the new
    string labels (RE / TVD / SPEARMAN) written by the mechanism files
  - Updated column name references to match mechanism output:
      iou_top_set   -> median_re  / spearman_rho / tvd / mean_tvd
      kendall_tau   -> spearman_rho
      mean_kl_div   -> mean_tvd
      top1_accuracy -> spearman_rho  (Q6 uses SPEARMAN)
  - Summary CSVs now read from:
      data/dp_{mechanism}_{database}/{variant}/{mechanism}_metric_summary.csv

Input:
    data/dp_gaussian_{database}/{variant}/gaussian_metric_summary.csv
    data/dp_laplace_{database}/{variant}/laplace_metric_summary.csv

Output:
    data/evaluation_results/{variant}/evaluation_summary_{database}.csv
    data/evaluation_results/{variant}/per_query_comparison_{database}.csv

Usage:
    python evaluate_dp_results.py --database mini --variant baseline
    python evaluate_dp_results.py --database full --variant advance
"""

import argparse
import os
import sys
from pathlib import Path

import numpy as np
import pandas as pd

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

# =============================================================================
#  PATHS
# =============================================================================

ROOT_DIR = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
DATA_DIR = os.path.join(ROOT_DIR, "data")
EVAL_DIR = os.path.join(DATA_DIR, "evaluation_results")

# =============================================================================
#  METRIC CONFIGURATION
#
#  metric_type is now the string written by the mechanism files:
#    "RE"       -> primary metric column: median_re   (lower is better)
#    "TVD"      -> primary metric column: tvd or mean_tvd (lower is better)
#    "SPEARMAN" -> primary metric column: spearman_rho (higher is better)
#
#  pass_threshold matches the shared evaluation framework:
#    RE       <= 0.25
#    TVD      <= 0.15
#    SPEARMAN >= 0.50
# =============================================================================

METRIC_CONFIG = {
    "RE": {
        "primary_col":     "median_re",
        "interpretation":  "lower is better",
        "pass_threshold":  0.25,
        "label":           "Median Relative Error",
    },
    "TVD": {
        # Pivot queries (Q7, Q8) write mean_tvd; histogram queries (Q10, Q11) write tvd.
        # We prefer mean_tvd if present, fall back to tvd.
        "primary_col":     "mean_tvd",
        "fallback_col":    "tvd",
        "interpretation":  "lower is better",
        "pass_threshold":  0.15,
        "label":           "Total Variation Distance",
    },
    "SPEARMAN": {
        "primary_col":     "spearman_rho",
        "interpretation":  "higher is better",
        "pass_threshold":  0.50,
        "label":           "Spearman Rho",
    },
}

TOLERANCE = 0.90  # adaptive threshold: must be within 90% of baseline value


# =============================================================================
#  HELPERS
# =============================================================================

def resolve_primary_col(metric_type: str, df_columns: list) -> str | None:
    """
    Return the primary metric column for a given metric_type, checking
    for fallbacks where applicable (e.g. TVD pivot vs histogram).
    """
    cfg = METRIC_CONFIG.get(metric_type)
    if cfg is None:
        return None
    col = cfg["primary_col"]
    if col in df_columns:
        return col
    fallback = cfg.get("fallback_col")
    if fallback and fallback in df_columns:
        return fallback
    return None


def make_baseline_mask(eps_series: pd.Series) -> pd.Series:
    """True where epsilon represents infinity."""
    eps_num = pd.to_numeric(eps_series, errors="coerce")
    m_num = np.isinf(eps_num)
    m_str = eps_series.astype(str).str.strip().str.lower().isin(
        {"inf", "infinity", "np.inf"}
    )
    return m_num | m_str


def summary_input_path(database: str, variant: str, mechanism: str) -> str:
    """
    Resolve the path to a mechanism summary CSV given the new folder structure:
        data/dp_{mechanism}_{database}/{variant}/{mechanism}_metric_summary.csv
    """
    folder = os.path.join(DATA_DIR, f"dp_{mechanism}_{database}", variant)
    return os.path.join(folder, f"{mechanism}_metric_summary.csv")


# =============================================================================
#  PER-QUERY EVALUATION
# =============================================================================

def evaluate_query(query_df: pd.DataFrame, query_num: int, mechanism: str) -> dict | None:
    """
    For a single query, compute performance statistics across all epsilon values.

    Returns a dict with:
      query_num, mechanism, metric_type, primary_col
      worst_value, worst_epsilon
      best_value,  best_epsilon
      baseline_value  (at eps=inf)
      adaptive_threshold
      mean_across_eps, std_across_eps
      n_pass_threshold, frac_eps_passing
      n_epsilons
    """
    if query_df is None or len(query_df) == 0:
        return None

    metric_type = str(query_df["metric_type"].iloc[0])
    primary_col = resolve_primary_col(metric_type, list(query_df.columns))

    if primary_col is None:
        print(f"    ⚠  Q{query_num} ({mechanism}): no known metric column for "
              f"metric_type='{metric_type}'. Columns: {list(query_df.columns)}")
        return None

    cfg = METRIC_CONFIG[metric_type]
    interpretation = cfg["interpretation"]
    pass_threshold = cfg["pass_threshold"]
    lower_is_better = (interpretation == "lower is better")

    # Build working series
    values_df = query_df[["epsilon", primary_col]].copy()
    values_df[primary_col] = pd.to_numeric(values_df[primary_col], errors="coerce")
    values_df = values_df.dropna(subset=[primary_col])

    if len(values_df) == 0:
        return None

    # Baseline (eps = inf)
    bmask = make_baseline_mask(values_df["epsilon"])
    baseline_rows = values_df[bmask]
    baseline_value = (
        float(baseline_rows[primary_col].iloc[0])
        if len(baseline_rows) > 0
        else None
    )

    # Worst / best across ALL epsilons (including inf)
    if lower_is_better:
        worst_idx = values_df[primary_col].idxmax()
        best_idx  = values_df[primary_col].idxmin()
    else:
        worst_idx = values_df[primary_col].idxmin()
        best_idx  = values_df[primary_col].idxmax()

    worst_value   = float(values_df.loc[worst_idx, primary_col])
    worst_epsilon = values_df.loc[worst_idx, "epsilon"]
    best_value    = float(values_df.loc[best_idx,  primary_col])
    best_epsilon  = values_df.loc[best_idx,  "epsilon"]

    mean_value = float(values_df[primary_col].mean())
    std_value  = float(values_df[primary_col].std())

    # Hard pass/fail against the shared evaluation framework threshold
    if lower_is_better:
        n_pass_hard = int((values_df[primary_col] <= pass_threshold).sum())
    else:
        n_pass_hard = int((values_df[primary_col] >= pass_threshold).sum())
    frac_pass_hard = n_pass_hard / len(values_df)

    # Adaptive threshold: within TOLERANCE of baseline
    if baseline_value is not None and not np.isnan(baseline_value):
        if lower_is_better:
            adaptive_thresh = baseline_value / TOLERANCE
            n_pass_adaptive = int((values_df[primary_col] <= adaptive_thresh).sum())
        else:
            adaptive_thresh = baseline_value * TOLERANCE
            n_pass_adaptive = int((values_df[primary_col] >= adaptive_thresh).sum())
        frac_pass_adaptive = n_pass_adaptive / len(values_df)
    else:
        adaptive_thresh    = None
        n_pass_adaptive    = None
        frac_pass_adaptive = None

    return {
        "query_num":          query_num,
        "mechanism":          mechanism,
        "metric_type":        metric_type,
        "primary_col":        primary_col,
        "worst_value":        round(worst_value,   4),
        "worst_epsilon":      worst_epsilon,
        "best_value":         round(best_value,    4),
        "best_epsilon":       best_epsilon,
        "baseline_value":     round(baseline_value, 4) if baseline_value is not None else None,
        "adaptive_threshold": round(adaptive_thresh, 4) if adaptive_thresh is not None else None,
        "mean_across_eps":    round(mean_value,    4),
        "std_across_eps":     round(std_value,     4),
        # Hard threshold (shared framework)
        "n_pass_hard":        n_pass_hard,
        "frac_pass_hard":     round(frac_pass_hard, 4),
        "pass_threshold":     pass_threshold,
        # Adaptive threshold (90% of baseline)
        "n_pass_adaptive":    n_pass_adaptive,
        "frac_pass_adaptive": round(frac_pass_adaptive, 4) if frac_pass_adaptive is not None else None,
        "n_epsilons":         len(values_df),
    }


# =============================================================================
#  COMPARISON TABLE  (Gaussian vs Laplace, per query per epsilon)
# =============================================================================

def create_comparison_table(gaussian_df: pd.DataFrame,
                             laplace_df: pd.DataFrame) -> pd.DataFrame | None:
    """
    For each (query_num, epsilon), compare Gaussian vs Laplace on the
    primary metric column.

    Returns a DataFrame with columns:
        query_num, epsilon, metric_type, primary_col,
        gaussian_value, laplace_value, difference, winner
    """
    if gaussian_df is None or laplace_df is None:
        print("  ⚠  Cannot build comparison table — one summary is missing.")
        return None

    # Merge on (query_num, epsilon)
    merged = pd.merge(
        gaussian_df,
        laplace_df,
        on=["query_num", "epsilon"],
        suffixes=("_g", "_l"),
        how="outer",
    )

    rows = []
    for _, row in merged.iterrows():
        query_num   = row["query_num"]
        epsilon     = row["epsilon"]
        metric_type = str(row.get("metric_type_g") or row.get("metric_type_l") or "")

        # Resolve column in merged frame
        all_cols    = list(merged.columns)
        primary_col = None
        cfg         = METRIC_CONFIG.get(metric_type)
        if cfg:
            cand = cfg["primary_col"]
            if f"{cand}_g" in all_cols:
                primary_col = cand
            else:
                fallback = cfg.get("fallback_col")
                if fallback and f"{fallback}_g" in all_cols:
                    primary_col = fallback

        if primary_col is None:
            continue

        gauss_val  = pd.to_numeric(row.get(f"{primary_col}_g"),  errors="coerce")
        laplace_val = pd.to_numeric(row.get(f"{primary_col}_l"), errors="coerce")

        if pd.isna(gauss_val) or pd.isna(laplace_val):
            continue

        difference = float(gauss_val) - float(laplace_val)
        lower_is_better = (METRIC_CONFIG[metric_type]["interpretation"] == "lower is better")

        if abs(difference) < 1e-7:
            winner = "tie"
        elif lower_is_better:
            winner = "gaussian" if gauss_val < laplace_val else "laplace"
        else:
            winner = "gaussian" if gauss_val > laplace_val else "laplace"

        rows.append({
            "query_num":      int(query_num),
            "epsilon":        epsilon,
            "metric_type":    metric_type,
            "primary_col":    primary_col,
            "gaussian_value": round(float(gauss_val),  4),
            "laplace_value":  round(float(laplace_val), 4),
            "difference":     round(difference,          4),
            "winner":         winner,
        })

    return pd.DataFrame(rows)


# =============================================================================
#  MAIN PIPELINE
# =============================================================================

def run_evaluation(database: str = "mini", variant: str = "baseline") -> None:
    """
    Full evaluation pipeline for one (database, variant) combination.

    Reads:
        data/dp_gaussian_{database}/{variant}/gaussian_metric_summary.csv
        data/dp_laplace_{database}/{variant}/laplace_metric_summary.csv

    Writes:
        data/evaluation_results/{variant}/evaluation_summary_{database}.csv
        data/evaluation_results/{variant}/per_query_comparison_{database}.csv
    """

    eval_out = os.path.join(EVAL_DIR, variant)
    Path(eval_out).mkdir(parents=True, exist_ok=True)

    print("=" * 70)
    print(f"DP EVALUATION  —  database={database}  variant={variant}")
    print("=" * 70)
    print(f"Output dir : {eval_out}")
    print(f"Tolerance  : {TOLERANCE}")
    print("=" * 70)

    # ── Load summaries ────────────────────────────────────────────────────────
    gauss_path  = summary_input_path(database, variant, "gaussian")
    laplace_path = summary_input_path(database, variant, "laplace")

    print("\n[1] Loading metric summaries...")
    print(f"    Gaussian : {gauss_path}")
    print(f"    Laplace  : {laplace_path}")

    gaussian_df = pd.read_csv(gauss_path)  if os.path.exists(gauss_path)  else None
    laplace_df  = pd.read_csv(laplace_path) if os.path.exists(laplace_path) else None

    if gaussian_df is None and laplace_df is None:
        print("  ✗ Neither summary found — run DP mechanism files first.")
        return

    if gaussian_df is not None:
        print(f"  ✓ Gaussian : {len(gaussian_df)} rows")
    else:
        print("  ⚠  Gaussian summary not found — skipping.")

    if laplace_df is not None:
        print(f"  ✓ Laplace  : {len(laplace_df)} rows")
    else:
        print("  ⚠  Laplace summary not found — skipping.")

    # ── Per-query evaluation ──────────────────────────────────────────────────
    print("\n[2] Evaluating queries...")
    results = []

    for mech, df in [("gaussian", gaussian_df), ("laplace", laplace_df)]:
        if df is None:
            continue
        for qnum in sorted(df["query_num"].unique()):
            qdf = df[df["query_num"] == qnum]
            result = evaluate_query(qdf, int(qnum), mech)
            if result:
                results.append(result)

    summary_df = pd.DataFrame(results)
    print(f"  ✓ Evaluated {len(summary_df)} (query, mechanism) pairs")

    # ── Comparison table ──────────────────────────────────────────────────────
    print("\n[3] Building Gaussian vs Laplace comparison table...")
    comparison_df = create_comparison_table(gaussian_df, laplace_df)
    if comparison_df is not None:
        print(f"  ✓ {len(comparison_df)} (query, epsilon) pairs compared")

    # ── Save ──────────────────────────────────────────────────────────────────
    print("\n[4] Saving results...")

    summary_path = os.path.join(eval_out, f"evaluation_summary_{database}.csv")
    summary_df.to_csv(summary_path, index=False)
    print(f"  ✓ {summary_path}")

    if comparison_df is not None:
        comp_path = os.path.join(eval_out, f"per_query_comparison_{database}.csv")
        comparison_df.to_csv(comp_path, index=False)
        print(f"  ✓ {comp_path}")

    # ── Console summary ───────────────────────────────────────────────────────
    print("\n" + "=" * 70)
    print("SUMMARY")
    print("=" * 70)

    for qnum in sorted(summary_df["query_num"].unique()):
        sub = summary_df[summary_df["query_num"] == qnum]
        mtype = sub["metric_type"].iloc[0]
        pcol  = sub["primary_col"].iloc[0]
        print(f"\nQ{qnum:02d}  metric_type={mtype}  column={pcol}")

        for _, row in sub.iterrows():
            baseline = row["baseline_value"]
            bstr = f"{baseline:.4f}" if pd.notna(baseline) else "N/A"
            fph  = row["frac_pass_hard"]
            fph_str = f"{fph*100:.0f}%" if pd.notna(fph) else "N/A"

            print(
                f"  {row['mechanism'].capitalize():8s}  "
                f"worst={row['worst_value']:.4f} @ ε={row['worst_epsilon']}  "
                f"best={row['best_value']:.4f} @ ε={row['best_epsilon']}  "
                f"baseline={bstr}  "
                f"pass(hard)={fph_str}"
            )

    print("\n" + "=" * 70)
    print("EVALUATION COMPLETE")
    print("=" * 70)


# =============================================================================
#  ENTRY POINT
# =============================================================================

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Evaluate DP mechanism results (supports baseline and advance variants)."
    )
    parser.add_argument(
        "--database", choices=["mini", "full"], default="mini",
        help="Which database results to evaluate."
    )
    parser.add_argument(
        "--variant", choices=["baseline", "advance"], default="baseline",
        help="Which mechanism variant to evaluate (baseline or advance)."
    )
    args = parser.parse_args()
    run_evaluation(database=args.database, variant=args.variant)